class Solution {
public:
    /**
     * 代码中的类名、方法名、参数名已经指定，请勿修改，直接返回方法规定的值即可
     * 给定一个字符串s，返回具有相同前缀后缀的子串的第二大长度，反之，返回-1即可。
     * @param s string字符串 代表题意中的字符串s
     * @return int整型
     */
    int solve(string s) {
        // write code here
        int n = s.size();
        int f[n + 1];
        memset(f,0x00,sizeof f);
        
        int i = 0, j = 1;
        while(j < n) {
            if(s[i] == s[j]) {
                f[j] = i + 1;
                i++;
                j++;
            } else {
                if(i == 0) {
                    f[j] = 0;
                    j++;
                    continue;
                }
                i = f[i - 1];
            }
        }
        
        return f[n - 1] == 0 ? -1 : f[n - 1];
    }
};


